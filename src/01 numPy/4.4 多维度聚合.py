'''
numpy研究的数据经常是多个维度的，这时候经常需要的操作是沿着某⼀轴进⾏操作，此时，聚合函数
都会有⼀个参数axis，⽤来表示需要沿着哪个轴进⾏聚合。
'''
import numpy as np

a = np.random.randint(100, size=(4, 5))
print("a = \n", a)
# 求最⼤值，每⼀⾏
b = a.max(axis=1)
print("a每⼀⾏的最⼤值是： ", b)
# 求每⼀列的和
c = a.sum(axis=0)
print("a每⼀列的和是：", c)

