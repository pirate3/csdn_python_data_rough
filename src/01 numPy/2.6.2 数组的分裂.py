'''
numpy.split: 默认沿着axis=0进⾏分裂，可以指定轴向
numpy.hsplit： 沿着横轴进⾏分裂
numpy.vsplit： 沿着竖轴进⾏分裂
'''
import numpy as np

a1 = np.arange(48).reshape((6,8))
print("a1 = \n", a1)
print("-" * 30)

# 沿着1轴分裂成三部分
a7, a8, a9 = np.split(a1, [2,5], axis=1)
print("a7 = \n", a7)
print("-" * 30)
print("a8 = \n", a8)
print("-" * 30)
print("a9 = \n", a9)

